  private _getLatLong(point: PointVM) {
    var geocoder = new google.maps.Geocoder();
    geocoder.geocode({ 'address': point.address + "," + point.city }, (results) => {
      if (!results || results.length <= 0) return this._errorLocalize(point.city);

      var latitude = results[0].geometry.location.lat();
      var longitude = results[0].geometry.location.lng();

      var image = {
        url: "https://cdn.signorprestito.it/pinmap/" + point.type + "_" + this.settings.markerColor + ".svg",
        scaledSize: new google.maps.Size(30, 44), // scaled size
        origin: new google.maps.Point(0, 0), // origin
        anchor: new google.maps.Point(15, 44) // anchor
      };

      //set marker
      var marker = new google.maps.Marker({
        position: { lat: latitude, lng: longitude },
        map: this.map,
        icon: image
      });

      google.maps.event.addDomListener(marker, 'click', () => {
        //open modal or emit click
        this.settings.openModal ? this._openModal(point) : this._emitEvent(point);
      });
    });
  }

